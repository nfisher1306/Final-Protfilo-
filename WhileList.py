# Description: This program will create a list called L that contains the numbers 0 to 10. On each iteration, the loop
# should append the current value of a counter variable to the list and then increase the counter by 1. The while loop
# should stop once the counter variable is greater than 10.
#
#
#

L = []

counter = 0

while counter < 10:
    number_input = 0
    L.append(number_input)
    print(L)
    number_input += 1
    counter += 1
